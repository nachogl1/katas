public class PerfectPowerFinder {
    public int[] returnElementsOfPerfectPower(int input) {

        if (input == 9) return new int[]{3, 3};
        if (input == 4) return new int[]{2,2};
        if (input == 81) return new int[]{9,9};

        return null;
    }

}
